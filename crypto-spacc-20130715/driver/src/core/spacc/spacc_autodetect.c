int spacc_autodetect(spacc_device *spacc)
{
   pdu_ddt ddt;
   dma_addr_t dma;
   void       *virt;
   int proclen, aadlen, ivsize, x, y, h, err, enc, hash;
   unsigned char key[64];

   if (spacc->config.version < 0x47) {
      unsigned char *modes = &spacc->config.modes[0];
      // we can't run autodetect so let's just jam values in...
      memset(spacc->config.modes, 0, sizeof (spacc->config.modes));

      switch (spacc->config.project) {
         case 0x1201:
             modes[CRYPTO_MODE_RC4_128]  = 4;      // RC4-128
             modes[CRYPTO_MODE_RC4_40]   = 1;      // RC4-40
             modes[CRYPTO_MODE_AES_ECB]  = 4|8|16; // AES 128/192/256
             modes[CRYPTO_MODE_AES_CBC]  = 4|8|16; // AES 128/192/256
             modes[CRYPTO_MODE_AES_CTR]  = 4|8|16; // AES 128/192/256
             modes[CRYPTO_MODE_AES_F8]   = 4|8|16; // AES 128/192/256
             modes[CRYPTO_MODE_AES_GCM]  = 4|8|16; // AES 128/192/256
             modes[CRYPTO_MODE_AES_CCM]  = 4|8|16; // AES 128/192/256
             modes[CRYPTO_MODE_DES_CBC]  = 2;      // DES 64bit
             modes[CRYPTO_MODE_DES_ECB]  = 2;      // DES 64bit
             modes[CRYPTO_MODE_3DES_CBC] = 8;      // 3DES 192bit
             modes[CRYPTO_MODE_3DES_ECB] = 8;      // 3DES 192bit
             modes[CRYPTO_MODE_KASUMI_ECB] = 4;
             modes[CRYPTO_MODE_KASUMI_F8]  = 4;
             modes[CRYPTO_MODE_SNOW3G_UEA2] = 4;

             modes[CRYPTO_MODE_HASH_MD5]        = 128|1|8|16;    // 16/32/64 byte support
             modes[CRYPTO_MODE_HMAC_MD5]        = 128|1|8|16;    // 16/32/64 byte support
             modes[CRYPTO_MODE_HASH_SHA1]       = 128|1|8|16;    // 16/32/64 byte support
             modes[CRYPTO_MODE_HMAC_SHA1]       = 128|1|8|16;    // 16/32/64 byte support
             modes[CRYPTO_MODE_HASH_SHA224]     = 128|1|8|16;    // 16/32/64 byte support
             modes[CRYPTO_MODE_HMAC_SHA224]     = 128|1|8|16;    // 16/32/64 byte support
             modes[CRYPTO_MODE_HASH_SHA256]     = 128|1|8|16;    // 16/32/64 byte support
             modes[CRYPTO_MODE_HMAC_SHA256]     = 128|1|8|16;    // 16/32/64 byte support
             modes[CRYPTO_MODE_HASH_SHA384]     = 128|1|8|16|32; // 16/32/64/128 byte support
             modes[CRYPTO_MODE_HMAC_SHA384]     = 128|1|8|16|32; // 16/32/64/128 byte support
             modes[CRYPTO_MODE_HASH_SHA512]     = 128|1|8|16|32; // 16/32/64/128 byte support
             modes[CRYPTO_MODE_HMAC_SHA512]     = 128|1|8|16|32; // 16/32/64/128 byte support
             modes[CRYPTO_MODE_MAC_KASUMI_F9]   = 128|1;         // 16 byte support
             modes[CRYPTO_MODE_MAC_SNOW3G_UIA2] = 128|1;         // 16 byte support
             modes[CRYPTO_MODE_MAC_CMAC]        = 128|1|4|8;     // 16/24/32
             modes[CRYPTO_MODE_MAC_XCBC]        = 128|1|4|8;     // 16/24/32
             modes[CRYPTO_MODE_SSLMAC_MD5]      = 128|1;         // 16
             modes[CRYPTO_MODE_SSLMAC_SHA1]     = 128|2;         // 20
             return 0;
         default:
             printk("spacc::This device does not support autodetect\n");
             return -1;
       }
   }

   // allocate DMA memory ...
   virt = pdu_dma_alloc(256, &dma);
   if (!virt) {
      return -1;
   }

   if (pdu_ddt_init(&ddt, 1)) {
      pdu_dma_free(256, virt, dma);
      return -1;
   }

   pdu_ddt_add(&ddt, dma, 256);

   for (x = 0; x < 64; x++) key[x] = x;

   for (x = 0; x < (sizeof(template)/sizeof(template[0])); x++) {
      spacc->config.modes[x] = template[x];
      if (template[x]) {
         for (y = 0; y < (sizeof(keysizes[0])/sizeof(keysizes[0][0])); y++) {
            if (template[x] & (1<<y)) {
               printk("Probing: %s,%d\n", names[x], keysizes[template[x]>>7][y]*8);

               // testing keysizes[y] with algo 'x' which should match the ENUMs above

               // skip RC4 if no contexts
               if ((x == CRYPTO_MODE_RC4_40 || x == CRYPTO_MODE_RC4_128) && (spacc->config.num_rc4_ctx == 0)) {
                  continue;
               }

               if (template[x] & 128) {
                  enc  = 0;
                  hash = x;
               } else {
                  enc  = x;
                  hash = 0;
               }

               h = spacc_open(spacc, enc, hash, -1, 0, NULL, NULL);
               if (h < 0) {
                  spacc->config.modes[x] &= ~(1<<y);
                  continue;
               }

               spacc_set_operation(spacc, h, OP_ENCRYPT, 0, IP_ICV_APPEND, 0, 0, 0);
               if (template[x] & 128) {
                  spacc_write_context(spacc, h, SPACC_HASH_OPERATION, key, keysizes[1][y] + (x == CRYPTO_MODE_MAC_XCBC ? 32 : 0), key, 16);
               } else {
                  uint32_t keysize;
                  ivsize = 16;
                  keysize = keysizes[0][y];
                  switch (x) {
                     case CRYPTO_MODE_AES_GCM: ivsize = 12; break;

                     case CRYPTO_MODE_KASUMI_ECB:
                     case CRYPTO_MODE_KASUMI_F8:
                     case CRYPTO_MODE_3DES_CBC:
                     case CRYPTO_MODE_3DES_ECB:
                     case CRYPTO_MODE_DES_CBC:
                     case CRYPTO_MODE_DES_ECB: ivsize = 8; break;

                     case CRYPTO_MODE_AES_XTS: keysize <<= 1; break;
                  }
                  spacc_write_context(spacc, h, SPACC_CRYPTO_OPERATION, key, keysize, key, ivsize);
               }
               spacc_set_key_exp(spacc, h);
               switch (x) {
                  case CRYPTO_MODE_ZUC_UEA3:
                  case CRYPTO_MODE_SNOW3G_UEA2:
                  case CRYPTO_MODE_MAC_SNOW3G_UIA2:
                  case CRYPTO_MODE_MAC_ZUC_UIA3:
                  case CRYPTO_MODE_KASUMI_F8:
                     spacc_set_auxinfo(spacc, h, 0, 0);
                     break;
                  case CRYPTO_MODE_MAC_KASUMI_F9:
                     spacc_set_auxinfo(spacc, h, 0, 8);
                     break;
               }

               memset(virt, 0, 256);

               // 16AAD/16PT or 32AAD/0PT depending on whether we're in a hash or not mode
               aadlen  = 16;
               proclen = 32;
               if (!enc) {
                  aadlen += 16;
               }
               switch (x) {
                  case CRYPTO_MODE_AES_CS1:
                  case CRYPTO_MODE_AES_CS2:
                  case CRYPTO_MODE_AES_CS3: proclen = 31; // fall through is intentional
                  case CRYPTO_MODE_AES_XTS: aadlen = 0;
               }
               err = spacc_packet_enqueue_ddt(spacc, h, &ddt, &ddt, proclen, 0, aadlen, 0, 0, 0);
               if (err == CRYPTO_OK) {
                  do {
                     err = spacc_packet_dequeue(spacc, h);
                  } while (err == CRYPTO_INPROGRESS);
               }
               if (err != CRYPTO_OK || !testdata[x].test[y].outlen || memcmp(testdata[x].test[y].data, virt, testdata[x].test[y].outlen)) {
                  spacc->config.modes[x] &= ~(1<<y);
               } else {
                  printk("spacc_kernel_autodetect::Detected %-20s with keysize %3d-bits\n", names[x], keysizes[template[x]>>7][y]*8);
               }
               spacc_close(spacc, h);
            }
         }
      }
   }
   pdu_ddt_free(&ddt);
   pdu_dma_free(256, virt, dma);
   return 0;
}

